/**
 * 기인물별 SIF 사례 조회
 */
function getSifCasesByHazard(hazardName) {
  return SIF_CASES.filter(c => c.기인물 === hazardName);
}

/**
 * 기인물별 추천 유해위험요인 (재해유발요인에서 추출)
 */
function getSifHazardSuggestions(hazardName) {
  const cases = getSifCasesByHazard(hazardName);
  const suggestions = [];
  cases.forEach(c => {
    c.재해유발요인.split(',').forEach(item => {
      const trimmed = item.trim();
      if (trimmed && !suggestions.some(s => s.text === trimmed)) {
        suggestions.push({ text: trimmed, 재해형태: c.재해형태, count: 1 });
      } else if (trimmed) {
        suggestions.find(s => s.text === trimmed).count++;
      }
    });
  });
  return suggestions.sort((a, b) => b.count - a.count);
}

/**
 * 기인물별 추천 안전조치 (위험성감소대책에서 추출)
 */
function getSifSafetySuggestions(hazardName) {
  const cases = getSifCasesByHazard(hazardName);
  const suggestions = [];
  cases.forEach(c => {
    c.위험성감소대책.split('▶').forEach(item => {
      const trimmed = item.trim();
      if (trimmed && !suggestions.some(s => s.text === trimmed)) {
        suggestions.push({ text: trimmed, count: 1 });
      } else if (trimmed) {
        suggestions.find(s => s.text === trimmed).count++;
      }
    });
  });
  return suggestions.sort((a, b) => b.count - a.count);
}
